export function formatRowspanAndColspan (tableData, tableKey) {
  const newArr = []
  // 分类检出tableData中的数据push到newArr中
  for (let i = 0; i < tableData.length;) {
    let count = 0
    for (let j = i; j < tableData.length; j++) {
      if (tableData[i][tableKey] === tableData[j][tableKey]) {
        count++
      }
    }
    newArr.push({
      data: tableData[i][tableKey],
      num: count
    })
    i += count
  }
  // 格式化newArr中的数据
  for (let k = 0; k < newArr.length; k++) {
    if (newArr[k].num > 1 || newArr[k].num === 0) {
      for (let w = k; w < newArr[k].num + k - 1; w++) {
        newArr.splice(w + 1, 0, {
          data: newArr[k].data,
          num: 0
        })
      }
    }
  }
  return newArr
}

